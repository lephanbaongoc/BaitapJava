package behavioural.iterator.vidu;

public class MonHoc {
    String ten;
    int soTc;

    public MonHoc(String ten, int soTc) {
        this.ten = ten;
        this.soTc = soTc;
    }

    @Override
    public String toString() {
        return "MonHoc{" +
                "ten='" + ten + '\'' +
                ", soTc=" + soTc +
                '}';
    }
}
